class IfSample{
    public static void main(String args[]){
        int x, y;
        x = 10;
        y = 20;

        if(x < y) System.out.println("x is less than y");
        if(x == y) System.out.println("x is equal to y");
        if(y > x) System.out.println("y is more than x");
    }
}